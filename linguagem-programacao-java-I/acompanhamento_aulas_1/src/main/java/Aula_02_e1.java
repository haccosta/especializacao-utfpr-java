public class Aula_02_e1 {
    public static void main (String args[]){
        int a = 0, b =15;
        do{
            System.out.println("O valor de A dentro do DO e:" +a);
            a++;
        }while(a<=b);
        System.out.println("");

        a = 0;
        do{
            System.out.println("O valor de B dentro do DO e:" +b);
            b--;
        }while(b>=a);
        System.out.println("");


    }
}

